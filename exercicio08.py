#Desenvolva um programa que calcule a potência de um número (base e expoente fornecidos pelo usuário)
num_base = float(input("Digite o número que será a base do cálculo: "))
num_expoente = float(input("Digite o número que será o expoente do cálculo: "))
resultado = num_base ** num_expoente
print(f"O resultado é: {resultado}")